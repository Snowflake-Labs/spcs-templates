-- rcr_customer_creds : setup.sql
-- SPCS sample where a Gradio service authenticates back to Snowflake using
-- customer-provided PATs (one per service user) stored in Snowflake SECRETs.
-- Run as ACCOUNTADMIN, then build/push the image, then run create_service.sql.
-- Doc: https://docs.snowflake.com/en/developer-guide/snowpark-container-services/spcs-execute-sql#connect-to-snowflake-by-using-customer-provided-credentials

USE ROLE ACCOUNTADMIN;

-- Customize names here. Defaults work for a clean sample install.
SET DB                  = 'app_db';
SET SCH                 = 'rcr';
SET WAREHOUSE           = 'app_wh';
SET COMPUTE_POOL        = 'app_compute_pool';
SET SERVICE_OWNER_ROLE  = 'service_owner_role';
SET FINANCE_ROLE        = 'finance_role';
SET MARKETING_ROLE      = 'marketing_role';
SET FINANCE_USER        = 'finance_service_user';
SET MARKETING_USER      = 'marketing_service_user';
SET NETWORK_RULE        = 'rcr_compute_pool_rule';
SET NETWORK_POLICY      = 'rcr_service_users_policy';

-- Roles
CREATE ROLE IF NOT EXISTS IDENTIFIER($SERVICE_OWNER_ROLE);
CREATE ROLE IF NOT EXISTS IDENTIFIER($FINANCE_ROLE);
CREATE ROLE IF NOT EXISTS IDENTIFIER($MARKETING_ROLE);

-- Database / schema / warehouse / compute pool, owned by finance_role.
CREATE DATABASE IF NOT EXISTS IDENTIFIER($DB);
GRANT OWNERSHIP ON DATABASE IDENTIFIER($DB) TO ROLE IDENTIFIER($FINANCE_ROLE) COPY CURRENT GRANTS;

USE DATABASE IDENTIFIER($DB);
CREATE SCHEMA IF NOT EXISTS IDENTIFIER($SCH);
GRANT OWNERSHIP ON SCHEMA IDENTIFIER($SCH) TO ROLE IDENTIFIER($FINANCE_ROLE) COPY CURRENT GRANTS;

CREATE WAREHOUSE IF NOT EXISTS IDENTIFIER($WAREHOUSE)
    WAREHOUSE_SIZE = XSMALL AUTO_SUSPEND = 60 AUTO_RESUME = TRUE INITIALLY_SUSPENDED = TRUE;

CREATE COMPUTE POOL IF NOT EXISTS IDENTIFIER($COMPUTE_POOL)
    MIN_NODES = 1 MAX_NODES = 1 INSTANCE_FAMILY = CPU_X64_XS AUTO_SUSPEND_SECS = 3600;

GRANT USAGE, MONITOR ON COMPUTE POOL IDENTIFIER($COMPUTE_POOL) TO ROLE IDENTIFIER($SERVICE_OWNER_ROLE);
GRANT USAGE ON WAREHOUSE IDENTIFIER($WAREHOUSE) TO ROLE IDENTIFIER($SERVICE_OWNER_ROLE);
GRANT USAGE ON WAREHOUSE IDENTIFIER($WAREHOUSE) TO ROLE IDENTIFIER($FINANCE_ROLE);
GRANT USAGE ON WAREHOUSE IDENTIFIER($WAREHOUSE) TO ROLE IDENTIFIER($MARKETING_ROLE);
--- finance_role is the owner so it already has access
GRANT USAGE ON DATABASE IDENTIFIER($DB) TO ROLE IDENTIFIER($MARKETING_ROLE);
GRANT USAGE ON SCHEMA IDENTIFIER($SCH)  TO ROLE IDENTIFIER($MARKETING_ROLE);

-- Demo table + row access policy keyed on CURRENT_ROLE().
USE ROLE IDENTIFIER($FINANCE_ROLE);
USE DATABASE  IDENTIFIER($DB);
USE SCHEMA    IDENTIFIER($SCH);
USE WAREHOUSE IDENTIFIER($WAREHOUSE);

CREATE OR REPLACE TABLE team_data (
    id NUMBER AUTOINCREMENT, team STRING NOT NULL, region STRING, metric STRING, amount NUMBER(18,2)
);

INSERT INTO team_data (team, region, metric, amount) VALUES
