    ('FINANCE',   'NA',   'Q1 expenses', 740400.00),
    ('FINANCE',   'EMEA', 'Q1 expenses', 318900.00),
    ('MARKETING', 'NA',   'leads (MQL)',   4820.00),
    ('MARKETING', 'EMEA', 'leads (MQL)',   2105.00),
