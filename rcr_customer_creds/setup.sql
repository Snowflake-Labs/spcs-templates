CREATE OR REPLACE ROW ACCESS POLICY team_access_policy
    AS (team STRING) RETURNS BOOLEAN ->
           (CURRENT_ROLE() = $FINANCE_ROLE   AND team = 'FINANCE')
        OR (CURRENT_ROLE() = $MARKETING_ROLE AND team = 'MARKETING');

ALTER TABLE team_data ADD ROW ACCESS POLICY team_access_policy ON (team);

-- finance_role is the owner so it already has access
GRANT SELECT ON TABLE team_data TO ROLE IDENTIFIER($MARKETING_ROLE);

-- Service users (one per team), each with its data-access role as default.
USE ROLE ACCOUNTADMIN;

CREATE USER IF NOT EXISTS IDENTIFIER($FINANCE_USER)
    TYPE = SERVICE DEFAULT_ROLE = $FINANCE_ROLE DEFAULT_WAREHOUSE = $WAREHOUSE;
CREATE USER IF NOT EXISTS IDENTIFIER($MARKETING_USER)
    TYPE = SERVICE DEFAULT_ROLE = $MARKETING_ROLE DEFAULT_WAREHOUSE = $WAREHOUSE;

GRANT ROLE IDENTIFIER($FINANCE_ROLE)   TO USER IDENTIFIER($FINANCE_USER);
GRANT ROLE IDENTIFIER($MARKETING_ROLE) TO USER IDENTIFIER($MARKETING_USER);

-- Network rule + policy that lets PATs authenticate from the SPCS compute pool.
-- CREATE IF NOT EXISTS so re-running the script doesn't fail with
-- "rule is associated with some network policies".
CREATE NETWORK RULE IF NOT EXISTS IDENTIFIER($NETWORK_RULE)
    TYPE = COMPUTE_POOL MODE = INGRESS VALUE_LIST = ($COMPUTE_POOL);

CREATE NETWORK POLICY IF NOT EXISTS IDENTIFIER($NETWORK_POLICY)
    ALLOWED_NETWORK_RULE_LIST = ($NETWORK_RULE);

ALTER USER IDENTIFIER($FINANCE_USER)   SET NETWORK_POLICY = $NETWORK_POLICY;
ALTER USER IDENTIFIER($MARKETING_USER) SET NETWORK_POLICY = $NETWORK_POLICY;

-- Generate PATs. The token value is returned only once and is captured into a
-- session variable so the next statement can write it into a SECRET object.
-- On re-runs replace ADD with ROTATE PROGRAMMATIC ACCESS TOKEN; the token name
-- can only exist once per user.
ALTER USER IDENTIFIER($FINANCE_USER)
    ADD PROGRAMMATIC ACCESS TOKEN finance_pat ROLE_RESTRICTION = $FINANCE_ROLE DAYS_TO_EXPIRY = 90;
SET FINANCE_PAT = (SELECT "token_secret" FROM TABLE(RESULT_SCAN(LAST_QUERY_ID())));

ALTER USER IDENTIFIER($MARKETING_USER)
    ADD PROGRAMMATIC ACCESS TOKEN marketing_pat ROLE_RESTRICTION = $MARKETING_ROLE DAYS_TO_EXPIRY = 90;
SET MARKETING_PAT = (SELECT "token_secret" FROM TABLE(RESULT_SCAN(LAST_QUERY_ID())));

-- Wrap each PAT in a Snowflake SECRET (TYPE = password). Mounted into the container later.
USE ROLE IDENTIFIER($SERVICE_OWNER_ROLE);
USE DATABASE IDENTIFIER($DB);
USE SCHEMA   IDENTIFIER($SCH);

CREATE OR REPLACE SECRET finance_pat_secret
    TYPE = password USERNAME = $FINANCE_USER PASSWORD = $FINANCE_PAT;
CREATE OR REPLACE SECRET marketing_pat_secret
    TYPE = password USERNAME = $MARKETING_USER PASSWORD = $MARKETING_PAT;

-- CALLER grants on service_owner_role: with customer-provided creds the service runs as the
-- calling user, but the privileges it can use are the intersection of the calling user's grants
-- and the service_owner_role's CALLER grants.
USE ROLE ACCOUNTADMIN;
GRANT CALLER USAGE  ON DATABASE  IDENTIFIER($DB)        TO ROLE IDENTIFIER($SERVICE_OWNER_ROLE);
GRANT CALLER USAGE  ON SCHEMA    IDENTIFIER($SCH)       TO ROLE IDENTIFIER($SERVICE_OWNER_ROLE);
GRANT CALLER USAGE  ON WAREHOUSE IDENTIFIER($WAREHOUSE) TO ROLE IDENTIFIER($SERVICE_OWNER_ROLE);
GRANT CALLER SELECT ON TABLE     team_data              TO ROLE IDENTIFIER($SERVICE_OWNER_ROLE);

-- Image repo for the container image you'll push.
USE ROLE IDENTIFIER($SERVICE_OWNER_ROLE);
USE DATABASE IDENTIFIER($DB);
USE SCHEMA   IDENTIFIER($SCH);
CREATE IMAGE REPOSITORY IF NOT EXISTS repo;
SHOW IMAGE REPOSITORIES;
